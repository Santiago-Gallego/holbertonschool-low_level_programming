#include <stdio.h>
/**
 * op_add - adds;
 * @a: int 1
 * @b: int 2
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtract;
 * @a: int 1
 * @b: int 2
 * Return: subtraction
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies;
 * @a: int 1
 * @b: int 2
 * Return: multiplication
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - op div;
 * @a: int 1
 * @b: int 2
 * Return: division answer
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulo
 * @a: int 1
 * @b: int 2
 * Return: mod
 */
int op_mod(int a, int b)
{
	return (a % b);
}
